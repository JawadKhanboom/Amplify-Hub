// Build-time generator for the Practical Resource Library download artifacts.
// Reads the version-controlled catalog and writes committed PDF/DOCX/XLSX files
// into assets/resources/downloads/. Run: npm run generate:resources

import { mkdir, writeFile, readdir, rm } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';
import { buildDocx, buildXlsx, buildPdf } from './lib/officegen.mjs';

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const siteRoot = path.resolve(scriptDir, '..', '..');
const catalogPath = path.join(siteRoot, 'assets', 'resource-catalog.js');
const outDir = path.join(siteRoot, 'assets', 'resources', 'downloads');

const catalogModule = await import(pathToFileURL(catalogPath).href);
const catalog = catalogModule.default || catalogModule.ResourceCatalog;
const resources = catalog.resources;

const builders = { pdf: buildPdf, docx: buildDocx, xlsx: buildXlsx };

await mkdir(outDir, { recursive: true });

// Remove stale files so deleted/renamed resources do not leave orphans.
const expected = new Set();
for (const resource of resources) {
  for (const download of resource.downloads || []) {
    expected.add(`${resource.id}.${download.format}`);
  }
}
for (const existing of await readdir(outDir).catch(() => [])) {
  if (existing === '.gitkeep') continue;
  if (!expected.has(existing)) await rm(path.join(outDir, existing));
}

let written = 0;
const summary = [];
for (const resource of resources) {
  const formats = [];
  for (const download of resource.downloads || []) {
    const builder = builders[download.format];
    if (!builder) throw new Error(`No builder for format "${download.format}" (resource ${resource.id})`);
    const buffer = builder({ ...resource, reviewDate: resource.reviewDate || catalog.reviewDate });
    const fileName = `${resource.id}.${download.format}`;
    await writeFile(path.join(outDir, fileName), buffer);
    formats.push(`${download.format}(${buffer.length}b)`);
    written++;
  }
  summary.push(`  ${resource.id}: ${formats.join(', ')}`);
}

console.log(`Generated ${written} download files for ${resources.length} resources in assets/resources/downloads/`);
console.log(summary.join('\n'));

// Emit an editorial-review checklist so every draft resource is presented for
// human review in the pull request (see rollout decision: content is draft until reviewed).
const catLabels = { script: 'Scripts', template: 'Templates', cheatsheet: 'Cheat Sheets', worksheet: 'Worksheets', interview: 'Interview Prep' };
const docLines = [];
docLines.push('# Practical Resource Library — editorial review checklist');
docLines.push('');
docLines.push('> **Status: DRAFT.** All 25 resources below are original first drafts generated for AmplifyHub');
docLines.push('> and have **not** been professionally reviewed yet. A human editor should read each resource');
docLines.push('> (open `resource.html?id=<id>` locally) and confirm it is accurate, beginner-safe, industry-neutral,');
docLines.push('> and free of dishonest, high-pressure, or fabricated-experience guidance before flipping');
docLines.push('> `status` from `draft` to `reviewed` in the catalog and re-running the migration generator.');
docLines.push('');
docLines.push('This file is generated by `scripts/generate-resource-files.mjs`; do not edit by hand.');
docLines.push('');
docLines.push('| ✓ | Category | ID | Title | Skill | Difficulty | Min | Downloads |');
docLines.push('|---|---|---|---|---|---|---|---|');
for (const r of resources) {
  const formats = (r.downloads || []).map((d) => d.format.toUpperCase()).join(' + ');
  docLines.push(`| [ ] | ${catLabels[r.category] || r.category} | \`${r.id}\` | ${r.title} | ${r.skill} | ${r.difficulty} | ${r.duration} | ${formats} |`);
}
docLines.push('');
docLines.push(`_${resources.length} resources · review target date ${catalog.reviewDate}._`);
const docPath = path.join(siteRoot, 'docs', 'resource-library-review.md');
await mkdir(path.dirname(docPath), { recursive: true });
await writeFile(docPath, docLines.join('\n') + '\n', 'utf8');
console.log(`Wrote editorial review checklist to docs/resource-library-review.md`);
